import UIKit

class ReadKindMessagesTableViewController: UITableViewController {
    var kindMessages : [KindMessage] = []
    override func viewDidLoad() {
        super.viewDidLoad()

    }
    
    func createKindMessages() -> [KindMessage] {
        let kind = KindMessage()
        kind.message = "Be kind to eachother"
        
        return [kind]
    }


    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return kindMessages.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "reuseIdentifier", for: indexPath)

        let kindMessage = kindMessages[indexPath.row]
        cell.textLabel?.text = kindMessage.message

        return cell
    }

